package day08;

public class Program03 {
	// 2.Looping statement ----->
    //                    i)For
    //					  ii)While
    //					  iii)Do-while
    // ii)WHILE STATEMENT :

	public static void main(String[] args) {
		int a=1;
		while (a<=5)
		{
			System.out.println("run a" );
			a=a+1;
		}
		
		
		int b=-10;
		while (b<=-5)
		{
			System.out.println("run b" );
			b=b+1;
		}
		
		double c=1.5;
		while (c<=5.5)
		{
			System.out.println("run c" );
			c=c+0.5;
		}
		
		
		
		
		
}
		

	}


