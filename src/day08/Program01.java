package day08;

public class Program01 {
	// iv) SWITCH statement : We should give only string and integer values.
    // String value :
	public static void main(String[] args) {
		String x="Tue";
		switch(x)
		
		{
		case "Sun":
		
			System.out.println("Day 01");
			break;
		
		case "Mon":
		
			System.out.println("Day 02");
			break;
		
		case "Tue":
		
			System.out.println("Day 03");
			break;
		
		case "Wed":
		
			System.out.println("Day 04");
			break;
		
		case "Thur":
		
			System.out.println("Day 05");
			break;
		
		case "Fri":
		
		System.out.println("Day 06");
			break;
		
		case "Sat":
		
			System.out.println("Day 07");
			break;
			
		default:
		System.out.println("Invalid comment");
		break;
			
		}
		
		
	
	}

}
