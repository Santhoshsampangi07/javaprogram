package day05;

public class Program01 {
	
	// operators ....topic
	// 3.Logical operators
	// &&  ||  ! 

	public static void main(String[] args) {
		int a=10;
		int b=20;
		int c=30;
		
		System.out.println(a>b && b>c);
		System.out.println(c>a || b>a);
		System.out.println(! (c>a || b>a));
		

	}

}

